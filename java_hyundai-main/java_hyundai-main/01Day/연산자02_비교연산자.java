package com.app4_연산자;

public class 연산자02_비교연산자 {

	public static void main(String[] args) {
		
		//2. 비교연산자 ==> 실행 결과는 논리값 ( true/false)
		
		int num = 10;
		int num2 = 3;

		System.out.println(num > num2); // 
		System.out.println(num >= num2); // 
		System.out.println(num < num2); // 
		System.out.println(num <= num2); // 
		System.out.println(num == num2); // 
		System.out.println(num != num2); // 같지 않냐
	}

}
