package com.app4_연산자;

public class 연산자03_대입연산자 {

	public static void main(String[] args) {
		
		//3. 대입연산자 
		
		int num = 10;

		int result = num;
		
		result += num;  // result = result + num; 
		result -= num;  // result = result - num; 
		result *= num;  // result = result * num; 
		result /= num;  // result = result / num; 
		result %= num;  // result = result % num; 

	}
}
