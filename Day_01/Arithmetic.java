package Day_01;

public class Arithmetic {

	public static void main(String[] args) {
		
		//1. 산술연산자
		
		int num = 10;
		int num2 = 3;

		System.out.println(num+num2); // 13
		System.out.println(num-num2); // 7
		System.out.println(num*num2); // 30
		System.out.println(num/num2); // 3
		System.out.println(num%num2); // 1 %는 나머지 연산자
	}

}
